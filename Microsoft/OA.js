function solution (A, K) {
  var n = A.length
  for (var i = 0; i < n - 1; i++) {
    if (A[i] + 1 < A[i + 1]) { return false }
  }
  if (A[0] !== 1 || A[n - 1] !== K) { return false } else { return true }
}

function addAll (string) {
  const array = string.split('')
  let sum = 0
  for (const ele of array) {
    sum += Number.parseInt(ele, 10)
  }
  return sum
}

